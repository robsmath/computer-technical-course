public void exibirRecordes(){
    int cont=0;
    ConectarMysql conectar = new ConectarMysql();
    String sql = "Select * from jogador order by jogadas;";
        try {
            ResultSet rs = conectar.conectar().prepareStatement(sql).executeQuery();
            rs.first();
            do{
              if(cont==0){
                  this.nome1.setText(rs.getString("nomeJogador"));
                  this.jogadas1.setText(String.valueOf(rs.getInt("jogadas")));cont++;
              } else if(cont==1){
                  this.nome2.setText(rs.getString("nomeJogador"));
                  this.jogadas2.setText(String.valueOf(rs.getInt("jogadas")));cont++;
              }
              else if(cont==2){
                  this.nome3.setText(rs.getString("nomeJogador"));
                  this.jogadas3.setText(String.valueOf(rs.getInt("jogadas")));cont++;
              }
              else if(cont==3){
                  this.nome4.setText(rs.getString("nomeJogador"));
                  this.jogadas4.setText(String.valueOf(rs.getInt("jogadas")));cont++;
              }
              else if(cont==4){
                  this.nome5.setText(rs.getString("nomeJogador"));
                  this.jogadas5.setText(String.valueOf(rs.getInt("jogadas")));cont++;
              }
            }while(rs.next());
        } catch (SQLException ex) {
            Logger.getLogger(HallFama.class.getName()).log(Level.SEVERE, null, ex);
        }
 conectar.desconectar();
}
